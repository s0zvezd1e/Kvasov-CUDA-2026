! ================================================================
! МОДУЛЬ CPU ТРАНСПОНИРОВАНИЯ (ЭТАЛОН)
! ================================================================
! Назначение: предоставляет эталонную реализацию транспонирования
!             на CPU для проверки корректности GPU версии
! ================================================================

module mat_transpose_cpu_module
    implicit none
    private
    public :: mat_transpose_cpu

contains

    ! ------------------------------------------------------------
    ! CPU ТРАНСПОНИРОВАНИЕ (ВСТРОЕННАЯ ФУНКЦИЯ FORTRAN)
    ! ------------------------------------------------------------
    ! Вход:  a - исходная матрица размером rows × cols
    ! Выход: b - транспонированная матрица размером cols × rows
    ! ------------------------------------------------------------
    ! Формула транспонирования:
    !   b(j,i) = a(i,j)  для всех i=1..rows, j=1..cols
    ! ------------------------------------------------------------
    subroutine mat_transpose_cpu(a, b)
        real, intent(in)  :: a(:,:)      ! исходная матрица (rows × cols)
        real, intent(out) :: b(:,:)      ! результат    (cols × rows)
        
        ! transpose() - встроенная функция Fortran
        ! Она делает то же самое, что и GPU ядро, но на процессоре
        b = transpose(a)
    end subroutine

end module mat_transpose_cpu_module
