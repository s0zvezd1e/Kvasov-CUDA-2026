! ================================================================
! МОДУЛЬ ВСПОМОГАТЕЛЬНЫХ ФУНКЦИЙ
! ================================================================
! Назначение: 
!   - заполнение матриц случайными числами
!   - сравнение результатов CPU и GPU
! ================================================================

module utils_module
    implicit none
    private
    public :: rnd_fill, check_equal_mat

    ! Интерфейс для перегрузки имени (оставляем для совместимости)
    interface rnd_fill
        module procedure rnd_fill_2d
    end interface

contains

    ! ------------------------------------------------------------
    ! ЗАПОЛНЕНИЕ МАТРИЦЫ СЛУЧАЙНЫМИ ЧИСЛАМИ
    ! ------------------------------------------------------------
    ! Вход:  a - матрица, которую нужно заполнить
    ! Выход: a - заполнена числами от 0 до 100
    ! ------------------------------------------------------------
    subroutine rnd_fill_2d(a)
        real, intent(out) :: a(:,:)      ! матрица на выходе
        
        call random_number(a)            ! числа от 0 до 1
        a = 100.0 * a                    ! умножаем на 100 -> диапазон 0..100
    end subroutine

    ! ------------------------------------------------------------
    ! СРАВНЕНИЕ ДВУХ МАТРИЦ С ДОПУСКОМ
    ! ------------------------------------------------------------
    ! Вход:  a, b - матрицы для сравнения
    ! Выход: equal = .TRUE. если все элементы совпадают с точностью 1e-4
    ! ------------------------------------------------------------
    logical function check_equal_mat(a, b) result(equal)
        real, intent(in) :: a(:,:), b(:,:)
        
        ! all() - возвращает .TRUE. если все элементы удовлетворяют условию
        ! abs()  - модуль разности
        equal = all(abs(a - b) <= 1.0e-4)
    end function

end module utils_module
