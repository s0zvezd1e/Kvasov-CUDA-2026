! ================================================================
! ГЛАВНАЯ ПРОГРАММА: ТРАНСПОНИРОВАНИЕ МАТРИЦЫ
! ================================================================
! 
! Что делает программа:
!   1. Создаёт матрицу A размером 1024×512
!   2. Заполняет её случайными числами
!   3. Транспонирует на CPU (эталон)
!   4. Транспонирует на GPU (наша реализация)
!   5. Сравнивает результаты
!
! ================================================================

program main
    ! ------------------------------------------------------------
    ! ПОДКЛЮЧАЕМ МОДУЛИ
    ! ------------------------------------------------------------
    use cudafor                       ! CUDA Fortran (нужен для GPU)
    use utils_module                  ! утилиты (заполнение, сравнение)
    use gpu_transpose                 ! наша GPU реализация
    use mat_transpose_cpu_module      ! CPU эталон
    
    implicit none
    
    ! ------------------------------------------------------------
    ! ПАРАМЕТРЫ ЗАДАЧИ
    ! ------------------------------------------------------------
    integer, parameter :: N_ROWS = 1024      ! количество СТРОК исходной матрицы
    integer, parameter :: N_COLS = 512       ! количество СТОЛБЦОВ исходной матрицы
    integer, parameter :: BLOCK_DIM = 16     ! размер блока (16×16 потоков)
    integer, parameter :: K_PREVIEW = 4      ! сколько элементов показывать
    
    ! ------------------------------------------------------------
    ! ПЕРЕМЕННЫЕ
    ! ------------------------------------------------------------
    real, allocatable :: a(:,:), b_gpu(:,:), b_cpu(:,:)  ! матрицы
    integer :: ra, ca, istat              ! вспомогательные
    
    ! ------------------------------------------------------------
    ! ШАГ 1: ВЫДЕЛЯЕМ ПАМЯТЬ
    ! ------------------------------------------------------------
    ! a     - исходная матрица (1024×512)
    ! b_gpu - результат транспонирования на GPU (512×1024)
    ! b_cpu - результат транспонирования на CPU (512×1024) для проверки
    ! ------------------------------------------------------------
    allocate(a(N_ROWS, N_COLS), b_gpu(N_COLS, N_ROWS), b_cpu(N_COLS, N_ROWS), stat=istat)
    
    ! ------------------------------------------------------------
    ! ШАГ 2: ЗАПОЛНЯЕМ ИСХОДНУЮ МАТРИЦУ СЛУЧАЙНЫМИ ЧИСЛАМИ
    ! ------------------------------------------------------------
    call rnd_fill(a)
    
    ! ------------------------------------------------------------
    ! ШАГ 3: ТРАНСПОНИРУЕМ НА GPU
    ! ------------------------------------------------------------
    call transpose_gpu(a, b_gpu, BLOCK_DIM)
    
    ! ------------------------------------------------------------
    ! ШАГ 4: ТРАНСПОНИРУЕМ НА CPU (ЭТАЛОН)
    ! ------------------------------------------------------------
    call mat_transpose_cpu(a, b_cpu)
    
    ! ------------------------------------------------------------
    ! ШАГ 5: ВЫВОДИМ ЛЕВЫЙ ВЕРХНИЙ УГОЛ ДЛЯ ВИЗУАЛЬНОЙ ПРОВЕРКИ
    ! ------------------------------------------------------------
    ra = min(K_PREVIEW, N_ROWS)       ! сколько строк показать
    ca = min(K_PREVIEW, N_COLS)       ! сколько столбцов показать
    
    call print_corner('Исходная матрица A:', a, ra, ca)
    call print_corner('Результат (CPU):', b_cpu, ca, ra)
    call print_corner('Результат (GPU):', b_gpu, ca, ra)
    
    ! ------------------------------------------------------------
    ! ШАГ 6: СРАВНИВАЕМ РЕЗУЛЬТАТЫ GPU И CPU
    ! ------------------------------------------------------------
    if (check_equal_mat(b_gpu, b_cpu)) then
        print *, '✓ РЕЗУЛЬТАТЫ СОВПАДАЮТ! Транспонирование выполнено верно.'
    else
        print *, '✗ ОШИБКА! Результаты не совпадают.'
    end if
    
    ! ------------------------------------------------------------
    ! ШАГ 7: ОСВОБОЖДАЕМ ПАМЯТЬ
    ! ------------------------------------------------------------
    deallocate(a, b_gpu, b_cpu)

contains

    ! ------------------------------------------------------------
    ! ПРОЦЕДУРА ПЕЧАТИ УГЛА МАТРИЦЫ
    ! ------------------------------------------------------------
    ! Выводит левый верхний угол матрицы размером nrows × ncols
    ! ------------------------------------------------------------
    subroutine print_corner(title, x, nrows, ncols)
        character(*), intent(in) :: title          ! заголовок
        real,         intent(in) :: x(:,:)         ! матрица
        integer,      intent(in) :: nrows, ncols   ! сколько строк и столбцов вывести
        
        integer :: i, j
        
        write(*,*)
        write(*,'(a)') trim(title)
        
        ! Выводим элементы
        do i = 1, nrows
            write(*,'(*(f10.4,1x))') (x(i, j), j = 1, ncols)
        end do
    end subroutine

end program main
