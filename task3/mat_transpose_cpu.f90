! ================================================================
! МОДУЛЬ CPU ТРАНСПОНИРОВАНИЯ (ЭТАЛОН)
! ================================================================
! Назначение: эталонная реализация транспонирования на CPU
!             для проверки GPU версий
! ================================================================

module mat_transpose_cpu_module
    implicit none
    private
    public :: mat_transpose_cpu

contains

    ! ------------------------------------------------------------
    ! CPU ТРАНСПОНИРОВАНИЕ (ВСТРОЕННАЯ ФУНКЦИЯ FORTRAN)
    ! ------------------------------------------------------------
    ! b(j,i) = a(i,j) для всех i,j
    ! ------------------------------------------------------------
    subroutine mat_transpose_cpu(a, b)
        real, intent(in)  :: a(:,:)   ! исходная матрица
        real, intent(out) :: b(:,:)   ! транспонированная
        
        b = transpose(a)              ! встроенная функция
    end subroutine

end module mat_transpose_cpu_module
