! НАЗНАЧЕНИЕ:
!   - Заполнение матриц случайными числами
!   - Сравнение результатов (поиск максимальной ошибки)
!   - Вывод метрик производительности
!
! ================================================================

module utils_module
    implicit none
    private
    public :: rnd_fill, max_abs_diff, print_metrics

contains

    ! ------------------------------------------------------------
    ! ЗАПОЛНЕНИЕ МАТРИЦЫ СЛУЧАЙНЫМИ ЧИСЛАМИ (ДВОЙНАЯ ТОЧНОСТЬ)
    ! ------------------------------------------------------------
    ! Вход:  a - матрица для заполнения
    ! Выход: a - заполнена случайными числами от 0 до 1
    ! ------------------------------------------------------------
    subroutine rnd_fill(a)
        real(8), intent(out) :: a(:,:)    ! матрица двойной точности
        call random_number(a)              ! числа от 0 до 1
    end subroutine

    ! ------------------------------------------------------------
    ! МАКСИМАЛЬНАЯ АБСОЛЮТНАЯ ОШИБКА МЕЖДУ ДВУМЯ МАТРИЦАМИ
    ! ------------------------------------------------------------
    ! Вход:  a, b - матрицы для сравнения
    ! Выход: diff - максимальная разница между элементами
    ! ------------------------------------------------------------
    real(8) function max_abs_diff(a, b) result(diff)
        real(8), intent(in) :: a(:,:), b(:,:)
        diff = maxval(abs(a - b))          ! ищем максимум по всей матрице
    end function

    ! ------------------------------------------------------------
    ! ПЕЧАТЬ МЕТРИК (ВРЕМЯ, ПРОИЗВОДИТЕЛЬНОСТЬ, BW)
    ! ------------------------------------------------------------
    ! Вход: 
    !   name - название метода
    !   t_sec - время в секундах
    !   gflops - производительность GFLOPS
    !   bytes_moved - объём переданных данных (байт)
    ! ------------------------------------------------------------
    subroutine print_metrics(name, t_sec, gflops, bytes_moved)
        character(*), intent(in) :: name
        real(8),      intent(in) :: t_sec, gflops, bytes_moved
        real(8) :: bw_gbs          ! пропускная способность (GB/s)

        if (t_sec > 0.d0) then
            bw_gbs = bytes_moved / t_sec / 1.d9
        else
            bw_gbs = 0.d0
        end if

        write(*,'(a25,2x,f10.6,2x,f12.3,2x,f12.3)') trim(name), t_sec, gflops, bw_gbs
    end subroutine

end module utils_module
