! ================================================================
! ЗАДАНИЕ 1: СЛОЖЕНИЕ ДВУХ МАССИВОВ НА CPU
! ================================================================
! 
! Что делает программа:
!   1. Создаёт три массива A, B, C размером 10 элементов
!   2. Заполняет A и B случайными числами
!   3. Выводит A и B на экран
!   4. Складывает A + B поэлементно, результат в C
!   5. Выводит результат C
!
! ================================================================

program add_arrays                ! Начало программы
    implicit none                 ! Обязательное объявление всех переменных
    
    ! ------------------------------------------------------------
    ! ОБЪЯВЛЕНИЕ ПЕРЕМЕННЫХ
    ! ------------------------------------------------------------
    
    integer, parameter :: n = 10   ! РАЗМЕР МАССИВОВ
                                    ! parameter = константа (не меняется)
                                    ! если нужно больше элементов - меняем 10 на другое число
    
    real, dimension(n) :: A, B, C   ! ТРИ МАССИВА ПО n ЭЛЕМЕНТОВ
                                    ! real = вещественные числа (с плавающей точкой)
                                    ! dimension(n) = размер n
    
    integer :: i                    ! СЧЁТЧИК ДЛЯ ЦИКЛОВ
    
    ! ------------------------------------------------------------
    ! ИНИЦИАЛИЗАЦИЯ ГЕНЕРАТОРА СЛУЧАЙНЫХ ЧИСЕЛ
    ! ------------------------------------------------------------
    
    call random_seed()              ! random_seed - "затравка" для генератора
                                    ! БЕЗ ЭТОЙ КОМАНДЫ: числа будут одинаковыми при каждом запуске
                                    ! С ЭТОЙ КОМАНДОЙ: числа разные при каждом запуске
    
    ! ------------------------------------------------------------
    ! ЗАПОЛНЕНИЕ МАССИВОВ A И B СЛУЧАЙНЫМИ ЧИСЛАМИ
    ! ------------------------------------------------------------
    
    call random_number(A)           ! random_number - заполняет массив A
                                    ! значения от 0.0 до 1.0
    
    call random_number(B)           ! то же самое для массива B
    
    ! ------------------------------------------------------------
    ! ВЫВОД МАССИВА A НА ЭКРАН
    ! ------------------------------------------------------------
    
    print *, 'Массив A:'             ! Вывод заголовка (звёздочка = автоматический формат)
    
    do i = 1, n                     ! ЦИКЛ от 1 до n (от 1 до 10)
        print '(f8.4)', A(i)        ! вывод одного элемента
        ! '(f8.4)' - формат: 
        !   f = вещественное число
        !   8 = всего 8 символов
        !   4 = 4 символа после запятой
        ! Пример: " 12.3456" (8 символов)
    end do
    
    print *                         ! ПУСТАЯ СТРОКА (для красоты вывода)
    
    ! ------------------------------------------------------------
    ! ВЫВОД МАССИВА B НА ЭКРАН
    ! ------------------------------------------------------------
    
    print *, 'Массив B:'
    
    do i = 1, n
        print '(f8.4)', B(i)
    end do
    
    print *
    
    ! ------------------------------------------------------------
    ! СЛОЖЕНИЕ МАССИВОВ A + B
    ! ------------------------------------------------------------
    !
    ! Покомпонентное сложение: C(1) = A(1) + B(1), C(2) = A(2) + B(2), ...
    !
    ! ------------------------------------------------------------
    
    do i = 1, n
        C(i) = A(i) + B(i)          ! каждый элемент C = сумма A и B
    end do
    
    ! ------------------------------------------------------------
    ! ВЫВОД РЕЗУЛЬТАТА НА ЭКРАН
    ! ------------------------------------------------------------
    
    print *, 'Результат сложения C = A + B:'
    
    do i = 1, n
        print '(f8.4)', C(i)
    end do
    
end program add_arrays            ! КОНЕЦ ПРОГРАММЫ
