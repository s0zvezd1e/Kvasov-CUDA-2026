! ================================================================
! ЗАДАНИЕ 1 (CUDA): СЛОЖЕНИЕ ДВУХ МАССИВОВ НА GPU
! ================================================================
!
! КЛЮЧЕВЫЕ ПОНЯТИЯ:
!   - CPU/HOST: обычный процессор
!   - GPU/DEVICE: видеокарта
!   - KERNEL: функция, которая бежит на GPU
!   - THREAD: один поток на GPU (один исполнитель)
!   - BLOCK: группа потоков
!   - GRID: группа блоков
!
! ПОРЯДОК ДЕЙСТВИЙ В ПРОГРАММЕ:
!   1. Заполнить массивы A и B на CPU
!   2. Скопировать A и B на GPU
!   3. Запустить ядро на GPU (сложение)
!   4. Скопировать результат обратно на CPU
!   5. Вывести на экран
!
! ================================================================

! ================================================================
! МОДУЛЬ С ЯДРОМ (вынесено отдельно, потому что attributes(global)
! нельзя использовать внутри contains в основной программе)
! ================================================================

module add_kernel_module
    use cudafor                        ! МОДУЛЬ CUDA (даёт доступ к функциям)
    implicit none
    
contains

    ! ================================================================
    ! ЯДРО (KERNEL) - ЭТА ФУНКЦИЯ БЕЖИТ НА GPU
    ! ================================================================
    !
    ! attributes(global) - ВАЖНО! Указывает, что это ядро для GPU
    !                      БЕЗ ЭТОГО код будет выполняться на CPU
    !
    ! КАЖДЫЙ ПОТОК НА GPU ВЫПОЛНЯЕТ ЭТУ ФУНКЦИЮ:
    !   - поток №1 обрабатывает элемент №1
    !   - поток №2 обрабатывает элемент №2
    !   - ... и так далее (всего n потоков)
    !
    ! ================================================================
    
    attributes(global) subroutine add_kernel(a, b, c, m)
        ! ------------------------------------------------------------
        ! ОБЪЯВЛЕНИЕ ПАРАМЕТРОВ
        ! ------------------------------------------------------------
        ! device - массив находится в ПАМЯТИ GPU (видеокарты)
        ! intent(in)  - входной параметр (только читаем)
        ! intent(out) - выходной параметр (только пишем)
        ! value       - целое число передаётся по значению
        ! ------------------------------------------------------------
        
        real, device, intent(in)  :: a(:)   ! Массив A на GPU
        real, device, intent(in)  :: b(:)   ! Массив B на GPU
        real, device, intent(out) :: c(:)   ! Массив C на GPU (результат)
        integer, value, intent(in) :: m     ! Размер массива
        
        integer :: i                         ! Номер потока (локальная переменная)
        
        ! ------------------------------------------------------------
        ! ВЫЧИСЛЕНИЕ НОМЕРА ПОТОКА
        ! ------------------------------------------------------------
        !
        ! ВСТРОЕННЫЕ ПЕРЕМЕННЫЕ CUDA:
        !   blockIdx%x  - номер блока (от 1 до grid%x)
        !   threadIdx%x - номер потока ВНУТРИ блока (от 1 до blockDim%x)
        !   blockDim%x  - размер блока (сколько потоков в блоке)
        !
        ! ФОРМУЛА ГЛОБАЛЬНОГО ИНДЕКСА:
        !   i = (номер_блока - 1) * размер_блока + номер_потока_в_блоке
        !
        ! В НАШЕМ СЛУЧАЕ:
        !   блок один (blockIdx%x=1), размер блока = m (10)
        !   получается: i = (1-1)*10 + threadIdx%x = threadIdx%x = 1,2,3...10
        !
        ! ------------------------------------------------------------
        
        i = (blockIdx%x - 1) * blockDim%x + threadIdx%x
        
        ! ------------------------------------------------------------
        ! ПРОВЕРКА ГРАНИЦ
        ! ------------------------------------------------------------
        !
        ! Если размер массива НЕ КРАТЕН размеру блока, последний блок
        ! будет содержать "лишние" потоки. Эта проверка защищает от
        ! выхода за пределы массива.
        !
        ! ------------------------------------------------------------
        
        if (i <= m) then
            c(i) = a(i) + b(i)             ! СЛОЖЕНИЕ
        end if
        
    end subroutine add_kernel

end module add_kernel_module


! ================================================================
! ОСНОВНАЯ ПРОГРАММА (ВЫПОЛНЯЕТСЯ НА CPU)
! ================================================================

program add_arrays_cuda
    use cudafor                          ! CUDA модуль
    use add_kernel_module                ! Наш модуль с ядром
    implicit none
    
    ! ------------------------------------------------------------
    ! ПАРАМЕТРЫ И ПЕРЕМЕННЫЕ
    ! ------------------------------------------------------------
    
    integer, parameter :: n = 10          ! РАЗМЕР МАССИВА (меняй здесь)
    
    ! Массивы на CPU (обычная память компьютера)
    real, dimension(n) :: A, B, C
    
    ! Массивы на GPU (память видеокарты)
    ! КЛЮЧЕВОЕ СЛОВО "device" означает "в памяти видеокарты"
    real, device, dimension(n) :: A_d, B_d, C_d
    
    integer :: i                          ! Счётчик для циклов
    integer :: istat                      ! Для проверки ошибок
    
    ! ------------------------------------------------------------
    ! ШАГ 1: ЗАПОЛНЯЕМ МАССИВЫ НА CPU
    ! ------------------------------------------------------------
    
    call random_seed()                    ! Инициализация генератора
    call random_number(A)                ! A = случайные числа 0..1
    call random_number(B)                ! B = случайные числа 0..1
    
    ! ------------------------------------------------------------
    ! ШАГ 2: КОПИРУЕМ ДАННЫЕ С CPU НА GPU
    ! ------------------------------------------------------------
    ! Это происходит ДО запуска ядра, чтобы GPU мог работать с данными
    !
    ! A_d = A   означает: скопировать каждый элемент из A (CPU) в A_d (GPU)
    ! Это называется H2D (Host to Device)
    ! ------------------------------------------------------------
    
    A_d = A                              ! Копируем A на видеокарту
    B_d = B                              ! Копируем B на видеокарту
    
    ! ------------------------------------------------------------
    ! ШАГ 3: ЗАПУСК ЯДРА НА GPU
    ! ------------------------------------------------------------
    !
    ! СИНТАКСИС: call имя_ядра<<<grid, block>>>(параметры)
    !
    !   grid  = dim3(1,1,1)   - всего 1 блок
    !   block = dim3(n,1,1)   - n потоков в блоке (10 потоков)
    !
    ! ПОТОКИ РАБОТАЮТ ПАРАЛЛЕЛЬНО:
    !   поток 1: C(1) = A(1) + B(1)
    !   поток 2: C(2) = A(2) + B(2)
    !   ... и так до 10
    !
    ! ------------------------------------------------------------
    
    call add_kernel<<<dim3(1,1,1), dim3(n,1,1)>>>(A_d, B_d, C_d, n)
    
    ! ------------------------------------------------------------
    ! ШАГ 4: СИНХРОНИЗАЦИЯ (ЖДЁМ ЗАВЕРШЕНИЯ GPU)
    ! ------------------------------------------------------------
    !
    ! cudaDeviceSynchronize() - функция, которая заставляет CPU ждать,
    ! пока GPU не закончит все вычисления.
    !
    ! БЕЗ ЭТОЙ КОМАНДЫ: CPU может попытаться прочитать результат ДО ТОГО,
    !                   как GPU закончит работу → получим МУСОР
    ! С ЭТОЙ КОМАНДОЙ:   CPU ждёт → ВСЁ ПРАВИЛЬНО
    !
    ! ------------------------------------------------------------
    
    istat = cudaDeviceSynchronize()      ! Ждём GPU (присваиваем результат в istat)
    
    ! ------------------------------------------------------------
    ! ШАГ 5: КОПИРУЕМ РЕЗУЛЬТАТ С GPU НА CPU
    ! ------------------------------------------------------------
    ! C = C_d   означает: скопировать каждый элемент из C_d (GPU) в C (CPU)
    ! Это называется D2H (Device to Host)
    ! ------------------------------------------------------------
    
    C = C_d                              ! Забираем результат с видеокарты
    
    ! ------------------------------------------------------------
    ! ШАГ 6: ВЫВОД РЕЗУЛЬТАТОВ НА ЭКРАН
    ! ------------------------------------------------------------
    
    print *, '=== СЛОЖЕНИЕ МАССИВОВ НА GPU ==='
    print *, ''
    
    print *, 'Массив A:'
    do i = 1, n
        print '(f8.4)', A(i)
    end do
    
    print *, ''
    
    print *, 'Массив B:'
    do i = 1, n
        print '(f8.4)', B(i)
    end do
    
    print *, ''
    
    print *, 'Результат C = A + B (вычислено на GPU):'
    do i = 1, n
        print '(f8.4)', C(i)
    end do
    
    print *, ''
    print *, '=== ВСЁ ВЫПОЛНЕНО УСПЕШНО ==='

end program add_arrays_cuda
